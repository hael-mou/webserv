# include "typedefs.hpp"
# include "Shared_ptr.hpp"

# include "IEventHandler.hpp"

# include "Directive.hpp"
# include "HttpServer.hpp"

/*******************************************************************************
 * HttpCluster Class :
*******************************************************************************/
namespace http
{
    class	Cluster
    {
    public:
        class Exception;

        typedef IEventHandler                                   IEH;
        typedef std::map<std::string, Handle>                   HandleMap;
        typedef std::vector<Directive::SharedPtr>               DirPtrVector;
        typedef std::vector<utls::shared_ptr<http::Server> >    ServerVector;
        typedef std::map<Handle, ServerVector>                  ServerMap;

        Cluster(Directive::SharedPtr httpDir);
        virtual ~Cluster(void);

        IEH::IEventHandlerQueue     createHandlers(void);

    private:
        ServerMap	mServers;
        HandleMap   mSockets;

        bool    _isSocketReadyCreated(const_string& aListen) const;
    };
};

/*******************************************************************************
	* Cluster Exception :
*******************************************************************************/
class	http::Cluster::Exception : public std::exception
{
public:
	Exception(std::string aMessage);
	virtual ~Exception(void) throw();
	const char* what(void) const throw();

private:
	std::string mMessage;
};

