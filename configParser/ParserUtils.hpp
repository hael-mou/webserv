namespace alg
{
	template <class T1, class T2>
	typename std::map<T1 *, T2 *>::iterator find(std::map<T1 *, T2 *> & map, T1 *key)
	{
		typename std::map<T1 *, T2 *>::iterator	iter;

		iter = map.begin();
		while (iter != map.end() && *iter->first != *key)
			++iter;
		return (iter);
	}

	template <class T1, class T2>
	typename std::map<T1, T2>::iterator find(std::map<T1, T2> & map, T1 key)
	{
		typename std::map<T1, T2>::iterator	iter;

		iter = map.begin();
		while (iter != map.end() && !(iter->first == key))
			++iter;
		return (iter);
	}
};

